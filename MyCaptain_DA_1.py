set1={1,2,3,3} #set1
set2={3,3,4,5,6,7} #set2
print("SET 1 = ",set1) #display of set1
print("SET 2 = ",set2) #display of set2
print("Union : ",set1.union(set2)) #display of union of set1 and set2
print("Intersection : ",set1.intersection(set2)) #display of intersection of set1 and set2
print("Difference : ",set1.difference(set2)) #display of difference of set1 and set2
print("Symmetric Difference : ",set1.symmetric_difference(set2)) #display of symmetric difference of set1 and set2